a = int(input())
b = int(input())

print( int(a) + int(b))
print( int(a) - int(b))
print( int(a) * int(b))
print( int(a) / int(b))  # Division (float): divides the first operand by the second
print( int(a) % int(b))  # Modulus: returns the remainder when the first operand is divided by the second
print( int(a) ** int(b)) # Power: Returns first raised to power second
print( int(a) // int(b)) # Division (floor): divides the first operand by the second
